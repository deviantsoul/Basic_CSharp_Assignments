using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Method_Assignment
{
    public class SimpleMath
    {
        public int plusMethodInt(int x, int y)
        {
            return x + y;
        }

        public int minusMethodInt( int x, int y)
        {
            return x - y;
        }

        public int multiplyMethodInt( int x, int y)
        {
            return x * y;
        }
            
    }
}
